function loadState() {
  const fallback = {
    sutraId: "heart",
    sectionIndex: 0,
    mode: "read",
    fontSize: 21,
    theme: "light",
    focus: false,
    bookmarks: [],
    notes: {},
    stats: {
      totalReads: 0,
      streak: 0,
      lastReadDate: ""
    }
  };

  try {
    return { ...fallback, ...JSON.parse(localStorage.getItem(STORAGE_KEY) || "{}") };
  } catch {
    return fallback;
  }
}

function saveState() {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(state));
}

function getLineText(line) {
  return typeof line === "string" ? line : line.text;
}

function getLineSearchText(line) {
  return typeof line === "string" ? line : `${line.text}${line.pinyin}`;
}

function normalizeSearchText(value) {
  return value
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/\s+/g, "");
}

function renderLine(line) {
  if (typeof line === "string") return `<p>${line}</p>`;
  return `
    <p class="mantra-line">
      <span class="mantra-text">${line.text}</span>
      <span class="pinyin">${line.pinyin}</span>
    </p>
  `;
}

function renderDetail(detail) {
  if (!Array.isArray(detail) || !detail.length) return "<p>这里会显示当前内容的详细解释。</p>";
  return detail
    .map((block) => {
      if (typeof block === "string") return `<p>${block}</p>`;
      const points = Array.isArray(block.points) && block.points.length
        ? `<ul>${block.points.map((point) => `<li>${point}</li>`).join("")}</ul>`
        : "";
      return `
        <section class="detail-section">
          <h4>${block.heading}</h4>
          <p>${block.body}</p>
          ${points}
        </section>
      `;
    })
    .join("");
}

function getDetailSearchText(detail) {
  if (!Array.isArray(detail)) return "";
  return detail
    .map((block) => {
      if (typeof block === "string") return block;
      return `${block.heading || ""}${block.body || ""}${Array.isArray(block.points) ? block.points.join("") : ""}`;
    })
    .join("");
}

function filteredSutras() {
  const query = els.search.value.trim();
  const normalizedQuery = normalizeSearchText(query);
  return SUTRAS.filter((sutra) => {
    const haystack = `${sutra.title}${sutra.shortTitle}${sutra.category}${sutra.keywords || ""}${sutra.sections.map((section) => section.lines.map(getLineSearchText).join("")).join("")}${sutra.detailTitle || ""}${getDetailSearchText(sutra.detail)}`;
    return haystack.includes(query) || normalizeSearchText(haystack).includes(normalizedQuery);
  });
}

function currentSutra() {
  return SUTRAS.find((sutra) => sutra.id === state.sutraId) || SUTRAS[0];
}

function currentSection() {
  const sutra = currentSutra();
  return sutra.sections[state.sectionIndex] || sutra.sections[0];
}

function renderLibrary() {
  const filtered = filteredSutras();

  els.sutraCount.textContent = `${filtered.length} 部`;
  els.sutraList.innerHTML = filtered
    .map((sutra) => {
      const active = state.mode === "read" && sutra.id === state.sutraId ? " active" : "";
      return `
        <button class="sutra-row${active}" type="button" data-sutra="${sutra.id}">
          <strong>${sutra.shortTitle}</strong>
          <span>${sutra.category} · ${sutra.time}</span>
        </button>
      `;
    })
    .join("");

  els.detailList.innerHTML = filtered
    .map((sutra) => {
      const active = state.mode === "detail" && sutra.id === state.sutraId ? " active" : "";
      return `
        <button class="detail-row${active}" type="button" data-detail="${sutra.id}">
          <strong>${sutra.detailTitle || `${sutra.shortTitle}详解`}</strong>
          <span>${sutra.category} · 学习说明</span>
        </button>
      `;
    })
    .join("");
}

function renderReader() {
  const sutra = currentSutra();
  const section = currentSection();
  const progress = ((state.sectionIndex + 1) / sutra.sections.length) * 100;
  const isDetailMode = state.mode === "detail";

  els.body.classList.toggle("detail-mode", isDetailMode);
  els.title.textContent = isDetailMode ? sutra.detailTitle || `${sutra.shortTitle}详解` : sutra.title;
  els.meta.textContent = isDetailMode ? `学习详解 · ${sutra.category}` : `${sutra.category} · ${sutra.time}`;
  els.sectionName.textContent = isDetailMode ? "详细解释" : section.title;
  els.progressBar.style.width = isDetailMode ? "100%" : `${progress}%`;
  els.prev.disabled = isDetailMode || state.sectionIndex === 0;
  els.next.disabled = isDetailMode || state.sectionIndex === sutra.sections.length - 1;
  els.purposeTitle.textContent = sutra.purposeTitle || "传统用途";
  els.purposeText.textContent = sutra.purpose || "这里会显示当前内容的传统用途。";
  els.explanationTitle.textContent = sutra.explanationTitle || "内容简释";
  els.explanationText.textContent = sutra.explanation || "这里会显示当前内容的简要解释。";

  if (isDetailMode) {
    els.content.innerHTML = `
      <div class="content-inner detail-article">
        <button class="text-button return-reading" type="button" data-return-reading="${sutra.id}">返回原文</button>
        <h3 class="section-title">${sutra.detailTitle || `${sutra.shortTitle}详解`}</h3>
        ${renderDetail(sutra.detail)}
      </div>
    `;
  } else {
    els.content.innerHTML = `
      <div class="content-inner">
        <h3 class="section-title">${section.title}</h3>
        ${section.lines.map(renderLine).join("")}
      </div>
    `;
  }
  els.content.scrollTop = 0;

  const noteKey = `${sutra.id}:${state.sectionIndex}`;
  els.noteText.value = state.notes[noteKey] || "";
  els.bookmarkButton.classList.toggle("active", hasBookmark(sutra.id, state.sectionIndex));
  renderBookmarks();
  saveState();
}

function renderSettings() {
  document.documentElement.style.setProperty("--reader-size", `${state.fontSize}px`);
  els.body.classList.toggle("dark", state.theme === "dark");
  els.body.classList.toggle("focus-mode", state.focus);
  els.themeToggle.classList.toggle("active", state.theme === "dark");
  els.focusToggle.classList.toggle("active", state.focus);
}

function renderStats() {
  els.streakDays.textContent = state.stats.streak;
  els.readCount.textContent = state.stats.totalReads;
}

function renderToday() {
  const dayIndex = Math.floor(Date.now() / 86400000) % SUTRAS.length;
  const sutra = SUTRAS[dayIndex];
  els.todayTitle.textContent = sutra.shortTitle;
  els.todayLine.textContent = getLineText(sutra.sections[0].lines[0]);
  els.readTodayButton.dataset.sutra = sutra.id;
}

function hasBookmark(sutraId, sectionIndex) {
  return state.bookmarks.some((item) => item.sutraId === sutraId && item.sectionIndex === sectionIndex);
}

function renderBookmarks() {
  if (!state.bookmarks.length) {
    els.bookmarkList.textContent = "还没有收藏。读到想反复看的段落，可以点上方书签。";
    return;
  }

  els.bookmarkList.innerHTML = state.bookmarks
    .slice()
    .reverse()
    .map((item) => {
      const sutra = SUTRAS.find((entry) => entry.id === item.sutraId);
      const section = sutra?.sections[item.sectionIndex];
      if (!sutra || !section) return "";
      return `
        <button class="bookmark-item" type="button" data-sutra="${sutra.id}" data-section="${item.sectionIndex}">
          ${sutra.shortTitle}
          <span>${section.title}</span>
        </button>
      `;
    })
    .join("");
}

function selectSutra(sutraId, sectionIndex = 0) {
  state.sutraId = sutraId;
  state.sectionIndex = sectionIndex;
  state.mode = "read";
  renderLibrary();
  renderReader();
}

function selectDetail(sutraId) {
  state.sutraId = sutraId;
  state.mode = "detail";
  renderLibrary();
  renderReader();
}

function changeSection(delta) {
  if (state.mode === "detail") return;
  const sutra = currentSutra();
  const nextIndex = Math.min(Math.max(state.sectionIndex + delta, 0), sutra.sections.length - 1);
  if (nextIndex === state.sectionIndex) return;
  state.sectionIndex = nextIndex;
  renderReader();
}

function toggleBookmark() {
  const sutraId = state.sutraId;
  const sectionIndex = state.sectionIndex;
  if (hasBookmark(sutraId, sectionIndex)) {
    state.bookmarks = state.bookmarks.filter((item) => item.sutraId !== sutraId || item.sectionIndex !== sectionIndex);
  } else {
    state.bookmarks.push({ sutraId, sectionIndex, createdAt: Date.now() });
  }
  renderReader();
}

function completeReading() {
  const today = new Date().toISOString().slice(0, 10);
  const yesterday = new Date(Date.now() - 86400000).toISOString().slice(0, 10);

  if (state.stats.lastReadDate !== today) {
    state.stats.streak = state.stats.lastReadDate === yesterday ? state.stats.streak + 1 : 1;
    state.stats.lastReadDate = today;
  }

  state.stats.totalReads += 1;
  renderStats();
  saveState();
  els.completeButton.textContent = "已记下";
  window.setTimeout(() => {
    els.completeButton.textContent = "诵读完成";
  }, 1200);
}

els.sutraList.addEventListener("click", (event) => {
  const row = event.target.closest("[data-sutra]");
  if (!row) return;
  selectSutra(row.dataset.sutra);
});

els.detailList.addEventListener("click", (event) => {
  const row = event.target.closest("[data-detail]");
  if (!row) return;
  selectDetail(row.dataset.detail);
});

els.content.addEventListener("click", (event) => {
  const button = event.target.closest("[data-return-reading]");
  if (!button) return;
  selectSutra(button.dataset.returnReading, state.sectionIndex);
});

els.bookmarkList.addEventListener("click", (event) => {
  const item = event.target.closest("[data-sutra]");
  if (!item) return;
  selectSutra(item.dataset.sutra, Number(item.dataset.section));
});

els.search.addEventListener("input", renderLibrary);
els.prev.addEventListener("click", () => changeSection(-1));
els.next.addEventListener("click", () => changeSection(1));
els.bookmarkButton.addEventListener("click", toggleBookmark);
els.completeButton.addEventListener("click", completeReading);

els.fontMinus.addEventListener("click", () => {
  state.fontSize = Math.max(17, state.fontSize - 1);
  renderSettings();
  saveState();
});

els.fontPlus.addEventListener("click", () => {
  state.fontSize = Math.min(28, state.fontSize + 1);
  renderSettings();
  saveState();
});

els.themeToggle.addEventListener("click", () => {
  state.theme = state.theme === "dark" ? "light" : "dark";
  renderSettings();
  saveState();
});

els.focusToggle.addEventListener("click", () => {
  state.focus = !state.focus;
  renderSettings();
  saveState();
});

els.noteText.addEventListener("input", () => {
  const noteKey = `${state.sutraId}:${state.sectionIndex}`;
  state.notes[noteKey] = els.noteText.value;
  saveState();
  els.saveState.textContent = "已保存";
  window.clearTimeout(window.noteSaveTimer);
  window.noteSaveTimer = window.setTimeout(() => {
    els.saveState.textContent = "自动保存";
  }, 900);
});

els.readTodayButton.addEventListener("click", () => {
  selectSutra(els.readTodayButton.dataset.sutra);
});

document.addEventListener("keydown", (event) => {
  if (event.target.matches("input, textarea")) return;
  if (event.key === "ArrowLeft") changeSection(-1);
  if (event.key === "ArrowRight") changeSection(1);
});

renderToday();
renderSettings();
renderStats();
renderLibrary();
renderReader();
